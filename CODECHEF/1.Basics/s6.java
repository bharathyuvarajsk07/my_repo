class Codechef {
    public static void main(String[] args) {
        // Integer literal for distance in kilometers
        Integer i=100;
        System.out.println(i);

        // Float literal for distance in miles
        Float f=62.14f;
        System.out.println(f);

        // Double literal for distance in light years
        Double d=9.4607e12;
        System.out.println(d);
    }
}
